function main() {
    Assigns(13)

}

function Assigns(Marks) {
    if (Marks <= 25) {
        console.log("Grade  F");
    } else if (Marks <= 35) {
        console.log("Grade  D");
    } else if (Marks <= 45) {
        console.log("Grade  C");
    } else if (Marks <= 65) {
        console.log("Grade  B");
    }
    if (Marks <= 85) {
        console.log("Grade  A");
    }

}
main();